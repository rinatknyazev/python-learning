# Создание словаря:
age = dict([['Кот матроскин', 5],['Пес Шарик',7],["Дядя Федор",12]])
# Перебор ключей:
for s in age.keys():
    print(f'!{s}: {age[s]}')
# Перебор значений:
for v in age.values():
    print(f'{v}',end='  ')
