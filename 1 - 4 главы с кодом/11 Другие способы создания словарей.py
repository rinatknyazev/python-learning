# Создание словаря:
color = dict([[(255,0,0),'Красный'],[(0,255,0),'Зеленый'],[(0,0,225),"Синий"]])
# Обращение к элементам словаря:
color [(255,255,0)] = 'Желтый'
print(f'(255,0,0): {color[(255,0,0)]}')
print(f'(255,255,0): {color[(255,255,0)]}')
